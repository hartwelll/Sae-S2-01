    private Rectangle2D hitboxBlock;
    private int id;
    private ArrayList<Rectangle2D> hitboxList;
    private ArrayList<Rectangle2D> hurtboxList;
    private int tailleTuile;

    public Map(){
        tailleTuile = 32;
        hitboxList = new ArrayList<>();
        hurtboxList = new ArrayList<>();
        rebuildHitboxes();
    }

    public void rebuildHitboxes(){
        hitboxList.clear();
        hurtboxList.clear();
        for(int i = 0; i < map.length; i++) {
            for (int j = 0; j < map[i].length; j++) {
                int x = j * tailleTuile;
                int y = i * tailleTuile;
                hitboxBlock = new Rectangle2D(x, y, tailleTuile, tailleTuile);
                if (map[i][j] != 0 && map[i][j] != 3) {
                    hitboxList.add(hitboxBlock);
                } else if (map[i][j] == 3) {
                    hurtboxList.add(hitboxBlock);
                }
            }
        }
    }

    public void setCase(int ligne, int colonne, int newId) {
        if (ligne >= 0 && ligne < map.length && colonne >= 0 && colonne < map[0].length) {
            map[ligne][colonne] = newId;
            rebuildHitboxes();
        }
    }

    public  int getColonne(){
        return map[0].length;
    }

    public int getLigne(){
        return map.length;
    }

    public int getCase(int x, int y){
        if (x >= 0 && x < map.length && y >= 0 && y < map[0].length){
            return map[x][y];
        }
        return 0;
    }

    public Rectangle2D getHitboxBlock(){
        return hitboxBlock;
    }

    public ArrayList<Rectangle2D> getHitboxList() {
        return hitboxList;
    }

    public ArrayList<Rectangle2D> getHurtboxList() {
        return hurtboxList;
    }

    public int getBlocId(int x, int y){
        int id =getCase(x, y);
        return id;
    }

    public int getTailleTuile() {
        return tailleTuile;
    }

    public int[][] getMap() {
        return map;
    }
