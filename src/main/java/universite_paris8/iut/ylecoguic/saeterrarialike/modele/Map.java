    private int tailleTuile;

    public Map(){
        tailleTuile = 32;
        hitboxList = new ArrayList<>();
        for(int i = 0; i < map.length; i++){
            for(int j = 0; j < map[i].length; j++){
                if(map[i][j] != 0) {
                    int x = j* tailleTuile;
                    int y = i* tailleTuile;
                    hitboxBlock = new Rectangle2D(x, y, tailleTuile, tailleTuile);
                    hitboxList.add(hitboxBlock);
                }
            }
        }
    }

